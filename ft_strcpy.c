#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*d;
	char	*s;

	s = (char *)src;
	d = (char *)dst;
	while (*s)
	{
		*d = *s;
		d++;
		s++;
	}
	*d = 0;
	return (dst);
}
