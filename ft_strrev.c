#include "libft.h"

char	*ft_strrev(char *src)
{
	char	*dst;
	char	*p;
	int		r;

	dst = ft_calloc(ft_strlen(src) + 1, 1);
	p = dst;
	r = ft_strlen(src);
	while (r)
	{
		*p = src[r - 1];
		p++;
		r--;
	}
	return (dst);
}
