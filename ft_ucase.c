#include "libft.h"

char	*ft_ucase(char *str)
{
	char	*dst;
	int		i;

	dst = ft_calloc(ft_strlen(str) + 1, 1);
	i = 0;
	while (str[i])
	{
		dst[i] = ft_toupper(str[i]);
		i++;
	}
	return (dst);
}
