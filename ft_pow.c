#include "libft.h"

double	ft_pow(double a, double e)
{
	double	r;

	if (!e)
		return (1);
	r = e > 0 ? a : 1;
	while ((long long)e)
	{
		r = e < 0 ? r / r : r * r; 
		e = e < 0 ? e + 1 : e - 1;
	}
	return (r);
}
